    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        newLostVPC = new NewLostVPC(){
            @Override
                public void action() {
                    if(this.jTextField1.getText().trim().isEmpty()){
                        this.setEnabled(false);
                        message = new LogMessage("Ô nhập còn trống"){
                            @Override
                            public void action() {
                                newLostVPC.setEnabled(true);
                                newLostVPC.requestFocus();
                                this.setVisible(false);
                            }
                        };
                        message.setLocationRelativeTo(null);
                        message.setVisible(true);
                        message.setDefaultCloseOperation(JFrame.DO_NOTHING_ON_CLOSE);   
                        }
                    int x = Integer.parseInt(this.jTextField1.getText().trim());
                    LostVisitorParkingCards a = new LostVisitorParkingCards(x);
                    boolean r = LostVisitorParkingCardsDAO.getInstance().insert(a);
                    if(r){
                        fillTable();
                        viewMain.setEnabled(true);
                        viewMain.requestFocus();
                        this.setVisible(false);
                    }else{
                        this.setEnabled(false);
                        message = new LogMessage("Thêm thất bại"){
                            @Override
                            public void action() {
                                newLostVPC.setEnabled(true);
                                newLostVPC.requestFocus();
                                this.setVisible(false);
                            }
                        };
                        message.setLocationRelativeTo(null);
                        message.setVisible(true);
                        message.setDefaultCloseOperation(JFrame.DO_NOTHING_ON_CLOSE);   
                        }
                    }
            @Override
                public void reject() {
                    viewMain.setEnabled(true);
                    viewMain.requestFocus();
                    this.setVisible(false);
                }
        };
        this.viewMain.setEnabled(false);
        this.newLostVPC.setLocationRelativeTo(null);
        this.newLostVPC.setVisible(true);
        this.newLostVPC.setDefaultCloseOperation(JFrame.DO_NOTHING_ON_CLOSE); 
    }//GEN-LAST:event_jButton5ActionPerformed



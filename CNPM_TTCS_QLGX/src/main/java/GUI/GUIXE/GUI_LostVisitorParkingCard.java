    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        LocalDate startDate ;
        if(jComboBox1.getSelectedIndex() == 0 || jComboBox2.getSelectedIndex() == 0 || jComboBox3.getSelectedIndex() == 0){
            startDate = null;
        }else{
            startDate = LocalDate.of(jComboBox3.getSelectedIndex()+2000,jComboBox2.getSelectedIndex() ,jComboBox1.getSelectedIndex());
        }
        LocalDate endDate ;
        if(jComboBox4.getSelectedIndex() == 0 || jComboBox5.getSelectedIndex() == 0 || jComboBox6.getSelectedIndex() == 0){
            endDate = null;
        }else{
            endDate = LocalDate.of(jComboBox6.getSelectedIndex()+2000,jComboBox5.getSelectedIndex() ,jComboBox4.getSelectedIndex());
        }
        if (startDate == null || endDate == null) {
            return;
        }

        if (startDate.isAfter(endDate)) {
            return;
        }

        // Truy vấn dữ liệu
        tableModel.setRowCount(0);
        String sql = "{CALL FillLostVisitorParkingCards(?, ?)}";

        try (Connection conn = OpenConnection.getConnection();
             CallableStatement stmt = conn.prepareCall(sql)) {

            stmt.setDate(1, java.sql.Date.valueOf(startDate));
            stmt.setDate(2, java.sql.Date.valueOf(endDate));

            try (ResultSet rs = stmt.executeQuery()) {
                while (rs.next()) {
                    tableModel.addRow(new String[] {
                        String.valueOf(rs.getInt("lost_visitor_parking_card_id")),
                        String.valueOf(rs.getInt("parking_session_id")),
                        String.valueOf(rs.getInt("vehicle_id")),
                        rs.getString("check_in_time")
                    });
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

